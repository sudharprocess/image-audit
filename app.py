import streamlit as st
import pandas as pd
from datetime import datetime
from io import BytesIO

st.set_page_config(layout="wide")
st.title("ğŸ“¸ Auditor Image Review Dashboard")

uploaded_file = st.file_uploader("Upload the CSV file", type=["csv"])
if not uploaded_file:
    st.stop()

usecols = ['Date', 'Outlet Code', 'Outlet Name', 'Distributor Code', 'Distributor Name', 'Salesman',
           'SDE', 'ASM', 'route_name', 'Is the outlet present or absent?', 'Absent Reason',
           'Image 1', 'Image 2', 'Image 3', 'Image 4', 'Image 5', 'Image 6', 'Auditor Name']
try:
    df = pd.read_csv(uploaded_file, encoding='ISO-8859-1', usecols=usecols)
except Exception as e:
    st.error(f"Error reading file: {e}")
    st.stop()

df['Date'] = pd.to_datetime(df['Date'], errors='coerce')
df = df.dropna(subset=['Date'])

# Filters
with st.form(key="filters"):
    col1, col2, col3 = st.columns(3)
    with col1:
        dist_code = st.selectbox("ğŸ¢ Distributor Code", ['All'] + sorted(df['Distributor Code'].dropna().unique().astype(str)))
        route_name = st.selectbox("ğŸ›£ï¸ Route Name", ['All'] + sorted(df['route_name'].dropna().unique().astype(str)))
        asm = st.selectbox("ğŸ‘” ASM", ['All'] + sorted(df['ASM'].dropna().unique().astype(str)))
    with col2:
        sde = st.selectbox("ğŸ‘¨â€ğŸ’¼ SDE", ['All'] + sorted(df['SDE'].dropna().unique().astype(str)))
        auditor = st.selectbox("ğŸ•µï¸ Auditor Name", ['All'] + sorted(df['Auditor Name'].dropna().unique().astype(str)))
        absent = st.selectbox("âŒ Absent Reason", ['All'] + sorted(df['Absent Reason'].dropna().unique().astype(str)))
    with col3:
        outlet = st.selectbox("ğŸ¬ Outlet Name", ['All'] + sorted(df['Outlet Name'].dropna().unique().astype(str)))
        salesman = st.selectbox("ğŸ§ Salesman", ['All'] + sorted(df['Salesman'].dropna().unique().astype(str)))
        from_date = st.date_input("ğŸ“… From Date", value=df['Date'].min().date())
        to_date = st.date_input("ğŸ“… To Date", value=df['Date'].max().date())

    global_search = st.text_input("ğŸ” Search across all columns")
    col4, col5 = st.columns(2)
    with col4:
        apply = st.form_submit_button("âœ… Apply Filters")
    with col5:
        reset = st.form_submit_button("ğŸ”„ Reset Filters")

if reset:
    st.experimental_rerun()

filtered = df.copy()
if apply:
    if dist_code != 'All':
        filtered = filtered[filtered['Distributor Code'] == dist_code]
    if route_name != 'All':
        filtered = filtered[filtered['route_name'] == route_name]
    if asm != 'All':
        filtered = filtered[filtered['ASM'] == asm]
    if sde != 'All':
        filtered = filtered[filtered['SDE'] == sde]
    if auditor != 'All':
        filtered = filtered[filtered['Auditor Name'] == auditor]
    if absent != 'All':
        filtered = filtered[filtered['Absent Reason'] == absent]
    if outlet != 'All':
        filtered = filtered[filtered['Outlet Name'] == outlet]
    if salesman != 'All':
        filtered = filtered[filtered['Salesman'] == salesman]
    filtered = filtered[(filtered['Date'].dt.date >= from_date) & (filtered['Date'].dt.date <= to_date)]
    if global_search:
        global_search = global_search.lower()
        filtered = filtered[filtered.apply(lambda row: row.astype(str).str.lower().str.contains(global_search).any(), axis=1)]

# Pagination
page_size = 10
total_pages = (len(filtered) - 1) // page_size + 1
page_num = st.number_input("Page", min_value=1, max_value=total_pages, step=1)
start = (page_num - 1) * page_size
end = start + page_size
paginated_df = filtered.iloc[start:end].copy()

# Download filtered data
buffer = BytesIO()
filtered.to_excel(buffer, index=False)
st.download_button("ğŸ“¥ Download Filtered Excel", data=buffer.getvalue(), file_name="filtered_data.xlsx", mime="application/vnd.openxmlformats-officedocument.spreadsheetml.sheet")

# Display data with images
for idx, row in paginated_df.iterrows():
    st.markdown("---")
    st.markdown(f" ğŸ”¢ S.No: {idx+1}")
    st.markdown(f"### ğŸ•µï¸ Auditor: **{row['Auditor Name']}**")
    st.markdown(f"""
    - ğŸ“… Date: {row['Date'].date()}
    - ğŸ¢ Distributor: {row['Distributor Name']} ({row['Distributor Code']})
    - ğŸ›£ï¸ Route: {row['route_name']}
    - ğŸ§ Salesman: {row['Salesman']}
    - ğŸ§‘â€ğŸ’¼ SDE: {row['SDE']} | ğŸ‘” ASM: {row['ASM']}
    - ğŸ¬ Outlet Name: {row['Outlet Name']} | Code: {row['Outlet Code']}
    - âŒ Absent Reason: {row['Absent Reason']}
    """)

    img_cols = st.columns(6)
    for i, img_col in enumerate(['Image 1', 'Image 2', 'Image 3', 'Image 4', 'Image 5', 'Image 6']):
        if img_col in row and pd.notna(row[img_col]) and str(row[img_col]).strip() != "":
            img_cols[i].image(row[img_col], caption=img_col, use_container_width=True)
        else:
            img_cols[i].write("ğŸ“· Image not available")